window.onload = function() {
    createGrid(portfolio);
};

function createGrid(data){
    let value = '';
    data.forEach((detail) => {
        value += 
            `<div class="gallery-grid__item">
                <img class="gallery-grid__item--image" src="${detail.photo}" alt="">
                <div class="item-detail">
                    <div class="item-detail__content">
                        <h2 class="item-detail__title">${detail.project}</h2>
                        <p class="item-detail__text">${detail.category}</p>
                    </div>
                </div>
            </div>`    
        ;
    });
    document.getElementById('content-all').innerHTML = value;
}

function filter(type) {
    var typePortfolio = [];
    if(type === 'all') {
        typePortfolio = portfolio;
    } else {
        typePortfolio = portfolio.filter(x => x.category == type);
    }

    createGrid(typePortfolio);
}

function view() {
    let viewGrid = document.getElementsByClassName('gallery-grid');
    viewGrid.classList.add('list-view');
}
